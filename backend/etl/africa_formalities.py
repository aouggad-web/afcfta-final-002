def get_formalities_for_line(country_iso3: str, category: str, chapter: str) -> list:
    """
    Return the list of required administrative documents for one tariff line.

    This is the single public entry-point used by the enrichment script.
    It works for all 54 African countries that have authority data defined in
    COUNTRY_AUTHORITIES above.

    DZA, MAR, and TUN already carry country-specific codes in their data
    (e.g. "910", "C01", "102") — this function will regenerate them using
    the generic IMPDEC/VETCERT/… scheme so that all countries share a
    consistent format.  The enrichment script skips re-enriching DZA/MAR/TUN
    since they already have richer, authority-validated data from their own
    dedicated ETL modules.

    Args:
        country_iso3: ISO3 code (e.g. "NGA", "KEN", "ZAF")
        category:     enhanced_v2 `category` field of the tariff line
        chapter:      2-digit HS chapter string (e.g. "01", "30", "87")

    Returns:
        List of formality dicts; always at least one entry (IMPDEC).
    """
    auth = COUNTRY_AUTHORITIES.get(country_iso3)
    if not auth:
        # Fallback: return bare import declaration
        return [
            {
                "code": "IMPDEC",
                "document_fr": "Déclaration d'Importation",
                "document_en": "Import Declaration",
                "authority_fr": f"Customs Authority — {country_iso3}",
                "authority_en": f"Customs Authority — {country_iso3}",
                "is_mandatory": True,
            }
        ]

    # Determine bucket
    ch = str(chapter).zfill(2)
    if category and category != "general":
        bucket = _category_to_bucket(category, ch)
    else:
        bucket = _chapter_to_bucket(ch)

    builder = _BUCKET_BUILDERS.get(bucket, _b_general)
    formalities = builder(auth)

    # COD (DRC) — OCC universal injection
    # The Office Congolais de Contrôle (OCC) issues a Certificat de Conformité
    # and levies a Redevance OCC (1.5 % of CIF value) on ALL imports into the
    # DRC, regardless of product category.
    # Source: Décret 090-0012 du 31/01/1990, Décret 09/11 du 24/04/2009,
    #         Décret 18/041 du 24/09/2018; DGDA circulaire no. 003/DGDA/2019.
    if country_iso3 == "COD":
        occ_authority = auth.get("occ", "Office Congolais de Contrôle (OCC)")
        formalities.append(
            {
                "code": "OCCDECL",
                "document_fr": (
                    "Certificat de Conformité OCC + Redevance OCC "
                    "(1,5 % CIF — obligatoire pour toutes importations en RDC)"
                ),
                "document_en": (
                    "OCC Conformity Certificate + OCC Special Levy "
                    "(1.5 % of CIF value — mandatory for all imports into DRC)"
                ),
                "authority_fr": occ_authority,
                "authority_en": occ_authority,
                "is_mandatory": True,
            }
        )

    return formalities
