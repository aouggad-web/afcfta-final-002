def _category_to_bucket(category: str, chapter: str) -> str:
    """Resolve a category+chapter pair to a formality bucket name."""
    if category in _ANIMAL_CATS:
        return "animal_products"
    if category in _FOOD_AGRI_CATS:
        return "food_agriculture"
    if category in _PHARMA_CATS:
        return "pharmaceuticals"
    if category in _AGRI_INPUT_CATS:
        return "agri_inputs"
    if category in _CHEMICAL_CATS:
        if chapter in ("31",):
            return "agri_inputs"
        return "chemicals"
    if category in _VEHICLE_MACH_CATS:
        return "vehicles_machinery"
    if category in _HYDRO_CATS:
        return "hydrocarbons"
    if category in _ARMS_CATS:
        return "arms_security"
    # Fallback: use chapter
    return CHAPTER_TO_BUCKET.get(str(chapter).zfill(2), "general")


def _chapter_to_bucket(chapter: str) -> str:
    """Map a 2-digit HS chapter to a formality bucket."""
    return CHAPTER_TO_BUCKET.get(str(chapter).zfill(2), "general")


